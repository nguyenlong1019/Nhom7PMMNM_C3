'''
    widgets: tiện ích
    Basic widgets (các phần tử giao diện (tiện ích) cơ bản)
    Label: hiển thị text hoặc image
    Button: thêm nút vào ứng dụng
    Canvas: dùng để vẽ hình ảnh, giao diện hoặc đồ họa (custom giao diện theo ý muốn)
    ComboBox: danh sách thả xuống các lựa chọn
    CheckButton: checkbox cỏ thể chọn nhiều option cùng lúc (vuông)
    RadioButton: chỉ chọn 1 option duy nhất (tròn)
    Entry: lấy input từ user
    Frame: dùng để chứa và tổ chức các widget
    Message: Hoạt động tương tự như Label nhưng không thể chỉnh sửa
    Scale: thanh trượt đồ họa
    Scrollbar: thêm thanh cuộn ngang hoặc dọc nếu content quá lớn và không thể đủ trong giao diện
    Spinbox: ví dụ chọn tuổi có thể tăng giảm
    Text: tương tự như entry nhưng cho phép nhập nhiều dòng
    Menu: Tạo menu điều hướng

    Nâng cao:
    Toplevel, LabelFrame, tkMessageBox, SpinBox, PanedWindow
''' 

